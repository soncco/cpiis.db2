    private void ColcocarAtributosEnTabla(){
        this.setTitle("Atributos de la relaci√≥n: "+ rf.getNameRelacion());
        String [] encabezado ={"Nro","Nombre",  "Tipo Atributo"};        
        DefaultTableModel modelo = new DefaultTableModel(encabezado, 0);
        for (int i = 0; i <rf.getRelacion().getAtributos().size(); i++) {
            Object[] o = {i+1, rf.getRelacion().getAtributos().get(i).getNombre(), rf.getRelacion().getAtributos().get(i).getTipo()};                    
            modelo.addRow(o);
        }  
        table_Atributos.setModel(modelo);
    }
